
# Arithmetic Operators
num1 = 10
num2 = 3
print("num1 + num2 is", num1 + num2)
print("num1 - num2 is", num1 - num2)
print("num1 * num2 is", num1 * num2)
print("num1 / num2 is", num1 / num2)
print("num1 % num2 is", num1 % num2)
print("num1 // num2 is", num1 // num2)  # 10/3 in integer form
print("num1 ** num2 is", num1 ** num2)  # 10*10*10

print(2**4)


